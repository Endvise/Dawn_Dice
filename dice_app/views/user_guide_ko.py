#!/usr/bin/env python3
"""
User Guide Page - Korean
일반 사용자용 이용 방법
"""

import streamlit as st
import auth


def show():
    """Show user guide (Korean)"""
    auth.require_login()

    st.title("📖 이용 방법")
    st.markdown("---")

    # 목차
    st.markdown("""
    ## 목차
    
    1. [회원가입](#회원가입)
    2. [로그인](#로그인)
    3. [예약 신청](#예약-신청)
    4. [내 예약 현황](#내-예약-현황)
    5. [비밀번호 변경](#비밀번호-변경)
    6. [유의사항](#유의사항)
    """)

    st.markdown("---")

    # 1. 회원가입
    st.markdown("## 1. 회원가입")
    st.markdown("""
    1. 로그인 화면에서 **회원가입** 버튼 클릭
    2. 다음 정보를 입력:
       - **사령관번호**: 10자리 숫자로 입력 (예: 1234567890)
       - **닉네임**: 원하는 닉네임
       - **서버**: 소속 서버 번호 (예: #095)
       - **연맹**: 연맹 이름 (없으면 비워두세요)
       - **비밀번호**: 8자 이상 입력
    3. **가입하기** 버튼 클릭
    """)

    st.markdown("---")

    # 2. 로그인
    st.markdown("## 2. 로그인")
    st.markdown("""
    1. 사령관번호(또는 아이디)와 비밀번호 입력
    2. **로그인** 버튼 클릭

    ### 비밀번호를 잊으셨나요?
    - **관리자에게 문의**하여 비밀번호 초기화를 요청하세요
    - 관리자가 초기화 후 새 비밀번호를 알려드립니다
    """)

    st.markdown("---")

    # 3. 예약 신청
    st.markdown("## 3. 예약 신청")
    st.markdown("""
    1. 예약 가능 시간에 **예약하기** 버튼 클릭
    2. 정보 확인 후 **제출** 버튼 클릭
    3. 예약 승인 완료!

    ### 예약 가능 시간
    - 상단의 **Reservations Open** 상태 확인
    - 예약 오픈 시간: 관리자가 설정한 시간
    - 예약 마감 시간: 설정된 시간 또는 정원 초과 시
    """)

    st.markdown("---")

    # 4. 내 예약 현황
    st.markdown("## 4. 내 예약 현황")
    st.markdown("""
    - **My Reservations** 메뉴에서 예약 현황 확인
    - 예약 번호(순서) 확인 가능
    - 대기 상태인 경우 대기 번호 확인
    """)

    st.markdown("---")

    # 5. 비밀번호 변경
    st.markdown("## 5. 비밀번호 변경")
    st.markdown("""
    1. **Change Password** 메뉴 이동
    2. 현재 비밀번호 입력
    3. 새 비밀번호 입력 (8자 이상)
    4. 새 비밀번호 다시 입력
    5. **변경** 버튼 클릭
    """)

    st.markdown("---")

    # 6. 유의사항
    st.markdown("## 6. 유의사항")
    st.markdown("""
    ### 선착순 예약제
    - 먼저 신청한 순서대로 예약이 확정됩니다
    - 정원(180명)이 초과되면 자동으로 대기자로 등록됩니다

    ### 블랙리스트
    - 블랙리스트에 등록된 사령관번호는 이용 불가합니다
    - 문의사항은 관리자에게 연락해주세요

    ### 계정 관련
    - 비밀번호 초기화 후 반드시 새 비밀번호로 변경하세요
    - 사령관번호는 변경이 불가능합니다
    """)

    st.markdown("---")
